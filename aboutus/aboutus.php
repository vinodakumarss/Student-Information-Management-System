<h1 align=center>R.V. College of Engineering</h1>
<p>Rashtreeya Vidyalaya College of Engineering (RVCE or RV College of Engineering, Rāshtrīya Vidyālaya Tāntrika Mahāvidyālaya) is an autonomous private technical co-educational college located in Bangalore, Karnataka, India. RVCE is recognized as center of excellence under Technical Education Quality Improvement Program by Government of India.<br>

Established in 1963, RVCE has 11 departments in engineering, one school in architecture, and a Master of Computer Applications department. It is affiliated to the Visvesvaraya Technological University, Belgaum. The undergraduate courses are granted academic autonomy by the university. RVCE is accredited by the All India Council for Technical Education and all its departments are accredited by the National Board of Accreditation. The college is managed by the Rashtreeya Shikshana Samiti Trust. Rashtreeya Shikshana Samiti Trust with its administrative offices located in Jayanagar, Bangalore. The Trust is chaired by Dr. Panduranga Setty.<br> </p>








